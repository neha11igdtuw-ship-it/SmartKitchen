window.fillCountrySelect = function fillCountrySelect(selectEl, selectedCode) {
  if (!selectEl || !window.SMARTKITCHEN_COUNTRIES) return;
  const cur = selectedCode || '';
  selectEl.innerHTML =
    '<option value="">Select country</option>' +
    window.SMARTKITCHEN_COUNTRIES.map(
      (c) =>
        `<option value="${c.code}"${c.code === cur ? ' selected' : ''}>${c.name}</option>`
    ).join('');
};
